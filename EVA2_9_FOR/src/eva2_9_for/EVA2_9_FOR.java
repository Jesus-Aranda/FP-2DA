package eva2_9_for;

public class EVA2_9_FOR {

    public static void main(String[] args) {
        //Tres parametros para funcionar
        //Punto inicial
        //Donde va acabar
        //Como me voy a mover
        //i ---> indice(arreglos)
        //++ sumar uno, -- restar uno
        for (int i = 10; i >= 1; i--) {
            System.out.println(i + " Hola mundo!!");
        }
        for (int i = 1; i <= 10; i--) {
            System.out.println(i + " Hola mundo!!");
        }
        
    }
}
